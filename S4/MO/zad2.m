clear x
clear y

x=-20:0.01:75;
%x=0:0.01:12;
%f=@(x)x.^2;
f=@(x)(1/3)*(x.^2)-((13/7)*x)+11;
%f=@(x)(1/3)*(x-3).^3+(x-4).^2;
%y=(1/3)*(x.^2)-((13/7)*x)+11;
%y=(1/3)*(x-3).^3+(x-4).^2;
plot(x,f(x))
hold on;

h=0.01;
df=@(x)(f(x+h)-f(x-h))/(2*h);
d2f=@(x)(f(x+h)-(2*f(x))+f(x-h))/(h.^2);

for k=1:100
    x(k+1)=x(k)-(df(x(k))/d2f(x(k)));
end

%y=(1/3)*(x(k+1).^2)-((13/7)*x(k+1))+11;
plot(x(k+1),f(x(k+1)),'*')

